import { HttpClient } from '@angular/common/http';
import { Injectable } from '@angular/core';
import { Observable } from 'rxjs';

@Injectable({
  providedIn: 'root'
})
export class CoinApiService {

  constructor(
    private http: HttpClient
  ) { }


  getCoinById(coinId: String) : Observable<ICoin> {
    return this.http.get<ICoin>('https://api.coingecko.com/api/v3/coins/' + coinId);
  }

  getCoinsList(): Observable<ICoinItem[]> {
    return this.http.get<ICoinItem[]>('https://api.coingecko.com/api/v3/coins/list');
  }

}

